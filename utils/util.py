last_valid_position = None

def drag_img(image, dragged_img, drag_hist, current_angle=0, scale=1.0):
    global last_valid_position
    img_height, img_width = dragged_img.shape[:2]
    frame_height, frame_width = image.shape[:2]
    
    if last_valid_position is None:
        last_valid_position = {
            'x': (frame_width - img_width) // 2,
            'y': (frame_height - img_height) // 2
        }
    
    # Draw drag history
    latest_point = None
    for index, point in enumerate(drag_hist):
        if point[0] != 0 and point[1] != 0:
            cv.circle(image, (point[0], point[1]), 1 + int(index / 2),
                      (152, 251, 251), 2)
            latest_point = point
    
    # Update position based on latest drag point
    if latest_point is not None:
        x = int(latest_point[0] - (img_width * scale)/2)
        y = int(latest_point[1] - (img_height * scale)/2)
        x = max(0, min(x, frame_width - int(img_width * scale)))
        y = max(0, min(y, frame_height - int(img_height * scale)))
        last_valid_position = {'x': x, 'y': y}
    
    # Scale the image before rotation
    if scale != 1.0:
        new_width = int(img_width * scale)
        new_height = int(img_height * scale)
        dragged_img = cv.resize(dragged_img, (new_width, new_height), 
                              interpolation=cv.INTER_LINEAR)
        img_height, img_width = dragged_img.shape[:2]
    
    # Rotate the dragged image
    if current_angle != 0:
        # Calculate rotation center
        center = (img_width // 2, img_height // 2)
        rotation_matrix = cv.getRotationMatrix2D(center, current_angle, 1.0)
        
        # Calculate new dimensions after rotation
        cos = np.abs(rotation_matrix[0, 0])
        sin = np.abs(rotation_matrix[0, 1])
        new_width = int((img_height * sin) + (img_width * cos))
        new_height = int((img_height * cos) + (img_width * sin))
        
        # Adjust rotation matrix
        rotation_matrix[0, 2] += (new_width / 2) - center[0]
        rotation_matrix[1, 2] += (new_height / 2) - center[1]
        
        # Perform rotation
        dragged_img = cv.warpAffine(dragged_img, rotation_matrix, (new_width, new_height),
                                  flags=cv.INTER_LINEAR, borderMode=cv.BORDER_TRANSPARENT)
        
        # Update dimensions
        img_height, img_width = dragged_img.shape[:2]
        
        # Adjust position to maintain center
        x = last_valid_position['x'] - (new_width - img_width) // 2
        y = last_valid_position['y'] - (new_height - img_height) // 2
        x = max(0, min(x, frame_width - img_width))
        y = max(0, min(y, frame_height - img_height))
        last_valid_position = {'x': x, 'y': y}
    
    # Extract the region where we'll overlay the image
    x, y = last_valid_position['x'], last_valid_position['y']
    
    # Ensure we don't exceed frame boundaries
    if x + img_width > frame_width:
        img_width = frame_width - x
    if y + img_height > frame_height:
        img_height = frame_height - y
        
    roi = image[y:y+img_height, x:x+img_width]
    
    # Handle potential size mismatch after rotation
    dragged_img = dragged_img[:img_height, :img_width]
    
    # Split the dragged image into color and alpha channels
    if dragged_img.shape[2] == 4:  # If BGRA
        b, g, r, alpha = cv.split(dragged_img)
        alpha = alpha / 255.0  # Normalize alpha to 0-1 range
        
        # Blend each channel
        roi_b = roi[:,:,0] * (1 - alpha) + b * alpha
        roi_g = roi[:,:,1] * (1 - alpha) + g * alpha
        roi_r = roi[:,:,2] * (1 - alpha) + r * alpha
        
        # Merge channels back
        blended = cv.merge([roi_b, roi_g, roi_r])
        
        # Ensure output is uint8
        blended = blended.astype(np.uint8)
        
        # Place blended result back into the image
        image[y:y+img_height, x:x+img_width] = blended
    else:  # If BGR
        image[y:y+img_height, x:x+img_width] = dragged_img
    
    return image

